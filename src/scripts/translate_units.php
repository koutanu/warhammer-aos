/* ============================================================
 * 3. 網羅性チェック
 * ============================================================ */
$allIds = $pdo->query('SELECT id FROM m_units ORDER BY id')->fetchAll(PDO::FETCH_COLUMN);
$missing = array_diff($allIds, array_keys($map));
$extra = array_diff(array_keys($map), $allIds);

if (!empty($missing)) {
    echo "WARNING: マップ未定義の id があります: " . implode(', ', $missing) . "\n";
}
if (!empty($extra)) {
    echo "WARNING: DBに存在しない id がマップにあります: " . implode(', ', $extra) . "\n";
}
echo 'INFO: DB units=' . count($allIds) . ', map entries=' . count($map) . "\n";

/* ============================================================
 * 4. name 更新（カタカナ（英語） 形式）
 *    英語部分は name_en（退避済み）から取得 → 冪等
 * ============================================================ */
$stmt = $pdo->prepare(
    "UPDATE m_units SET name = CONCAT(:kana, '（', name_en, '）') WHERE id = :id"
);

$updated = 0;
foreach ($map as $id => $kana) {
    if (!in_array($id, $allIds)) {
        continue;
    }
    $stmt->execute(['kana' => $kana, 'id' => $id]);
    $updated += $stmt->rowCount();
}
echo "OK: updated $updated rows\n";

/* ============================================================
 * 5. 更新後サンプル出力
 * ============================================================ */
echo "\n=== サンプル（各ファクション先頭10件） ===\n";
$sample = $pdo->query(
    'SELECT u.id, u.name, f.name AS faction
     FROM m_units u
     LEFT JOIN m_factions f ON f.id = u.faction_id
     ORDER BY u.faction_id, u.id'
)->fetchAll(PDO::FETCH_ASSOC);

$perFaction = [];
foreach ($sample as $row) {
    $fac = $row['faction'] ?? '不明';
    $perFaction[$fac] = ($perFaction[$fac] ?? 0) + 1;
    if ($perFaction[$fac] <= 10) {
        echo "[{$fac}] {$row['id']} | {$row['name']}\n";
    }
}

echo "\nDone.\n";
